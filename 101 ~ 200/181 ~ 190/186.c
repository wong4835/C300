#include <stdio.h>
#include <math.h>

void main(void)
{
    printf("floor(1.0) : %g\n", floor(1.0));
    printf("floor(1.1) : %g\n", floor(1.1));
    printf("floor(1.9) : %g\n", floor(1.9));
    printf("floor(2.5) : %g\n", floor(2.5));
    printf("floor(-2.5) : %g\n", floor(-2.5));
    printf("floor(-3.0) : %g\n", floor(-3.0));
}
